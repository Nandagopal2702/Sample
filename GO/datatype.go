package main

import (
	"fmt"
)

func main() {
	var a bool = true      // Boolean type
	var b int = 20         // integer type
	var c float32 = 3.14   // float type
	var d string = "Nandu" //string type

	fmt.Println("Boolean : ", a)
	fmt.Println("Integer : ", b)
	fmt.Println("Float : ", c)
	fmt.Println("String : ", d)
}
